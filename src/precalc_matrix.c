#include "fdf.h"

void	ft_precalc_view(t_mat4 mat, int index)
{
	static const t_mat4	view[2] = {
	{0},
	{{0.707107, 0.707107, 0, 0},
	{-0.577353, 0.577353, 0.577345, 0},
	{0.408244, -0.408244, 0.816501, 0},
	{0, 0, 0, 1}
	}};
	t_mat4				temp;

	ft_init_mat4(temp);
	ft_mult_mat4(view[index], mat, temp);
	ft_copy_mat4(temp, mat);
}
